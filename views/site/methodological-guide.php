    <h3 class="header-text" id="development_of_hypotheses_of_value_propositions">6.<span>Этап 5. Разработка гипотез ценностных предложений</span></h3>

    <div class="container-list">

        <div class="simple-block">
            <p>
                <span>Задача:</span>
                Сформировать список гипотез ЦП, которые должны быть протестированы на следующей стадии.
            </p>
            <p>
                <span>Результат:</span>
                Одна или несколько сформулированных гипотез ЦП, внесенные в форму на платформе Spaccel.ru.
            </p>
        </div>

        <p>
            Отправной точкой для создания нового или улучшенного ценностного предложения может служить все, что угодно.
            Она может появиться из ценной информации от потребителей, исследования прототипов или из каких-то других источников.
            Не увлекайтесь первоначальными идеями, поскольку они неизбежно радикально трансформируются в ходе прототипирования,
            исследований потребителя и тестирования.
        </p>

        <div class="bold">1. Визуализация идей с помощью «набросков на салфетках».</div>

        <div class="container-text">
            <ul>
                <li class="pl-15">Это исследовательский инструмент. На одной салфетке – одна идея, не важно, как ее выполнят.</li>
                <li class="pl-15">Их нужно сделать много (5-20), сколько рука возьмет. Только суть идеи, а не то, как будет работать.</li>
            </ul>
        </div>

        <p class="bold">2. Быстрое представление возможностей с помощью шаблона.</p>

        <p class="blue">
            <span class="bold">Наш</span> _(товар/услуга) <span class="bold">помогает</span> (потребительскому сегменту), <span class="bold">которые хотят</span> (выполнить задачу),
            <span class="bold">так, что</span> (снижает, избавляет(проблему)) <span class="bold">и</span> (увеличивает, позволяет (выгоду)) <span class="bold">в отличии от</span> (конкурирующее ЦП).
        </p>

        <p class="bold">3. Метод «Шесть шляп мышления» Эдварда де Боно (материалы широко представлены в сети).</p>
        <p class="bold">4. Метод разработки ценностного предложения, предложенный А. Остервальдером.</p>

        <br>

        <p>
            В данном материале предлагается создать Ценностное предложение в формате п.2 «Быстрое представление возможностей
            с помощью шаблона». А для лучшего понимания структуры потребностей / проблем потребителя предлагается рассмотреть
            сокращенный материал о подходах, предлагаемый А. Остервальдером.
        </p>

        <p class="bold">Структура профиля потребителя с точки зрения его потребностей</p>

        <ol>
            <li class="bold"><span class="pl-15">Задачи потребителя</span>
                <span class="normal"> &#8212; то, что потребитель делает в профессиональной и личной жизни.
                    Три основных типа задач потребителя и дополнительные задачи:</span>
                <div class="container-text">
                    <ol>
                        <li class="bold"><span class="pl-15">Функциональные задачи</span>
                            <span class="normal"> &#8212; когда выполняют конкретное задание: пишут отчет и т.д.</span>
                        </li>
                        <li class="bold"><span class="pl-15">Социальные  задачи</span>
                            <span class="normal"> &#8212; когда стараются хорошо выглядеть, добиться влияния или статуса в обществе. Это про образ человека.</span>
                        </li>
                        <li class="bold"><span class="pl-15">Личностные/эмоциональные</span>
                            <span class="normal"> задачи. Когда потребитель старается достичь определенного эмоционального состояния, как спокойствие, безопасность, гарантии.</span>
                        </li>
                        <li class="bold"><span class="pl-15">Дополнительные задачи</span>
                            <span class="normal"> &#8212; связано с приобретением и потреблением ценностей в качестве покупателей или профессионалов:</span>
                            <div class="container-text">
                                <ol>
                                    <li><span class="normal pl-15">Покупатель ценности – принятие решения о покупке.</span></li>
                                    <li><span class="normal pl-15">Партнер по созданию ценности. Участие в создании ценностей – публикация обзоров или отзывов на товары. Участие в разработке товара.</span></li>
                                    <li><span class="normal pl-15">Лицо, передающее ценность – конечный этап жизненного цикла цен предложения - отмена подписки, утилизация товара, передача его другим лицам, перепродажа и т.д.</span></li>
                                </ol>
                            </div>
                        </li>
                    </ol>
                </div>
                <p>Степень измерения важности задачи: <span class="normal">маловажная --- важная.</span></p>
            </li>
            <li class="bold"><span class="pl-15">Проблемы потребителя</span>
                <span class="normal"> &#8212; все, что волнует потребителя до, во время и после выполнения задачи или просто мешает ее выполнить. Также риски, возможность неудачного исхода, т.к. задача выполнена плохо или не выполнена вообще.</span>
                <div class="normal">Три типа потребительских проблем:</div>
                <div class="container-text">
                    <ol>
                        <li class="bold"><span class="pl-15">Нежелательные результаты и свойства:</span>
                            <div class="container-text">
                                <ol>
                                    <li><span class="normal pl-15">Функциональные (плохо работает, побочные эффекты).</span></li>
                                    <li><span class="normal pl-15">Социальные (когда я это делаю, выгляжу плохо), эмоциональными (когда я это делаю, чувствую себя плохо).</span></li>
                                    <li><span class="normal pl-15">Сопутствующие (раздражает, что нужно идти за этим в магазин, скучный дизайн, нужно одеваться в зал).</span></li>
                                </ol>
                            </div>
                        </li>
                        <li class="bold"><span class="pl-15">Препятствия</span>
                            <span class="normal"> &#8212; то, что не дает выполнить или замедляет (не успеваю сделать аккуратно, вариант не по карману).</span>
                        </li>
                        <li class="bold"><span class="pl-15">Риски</span>
                            <span class="normal"> &#8212; что-то может привести к серьезным нежелательным последствиям.</span>
                        </li>
                    </ol>
                </div>
                <div>Способ измерения серьезности проблемы: <span class="normal">серьезная --- умеренная.</span></div>
            </li>
        </ol>

        <p class="blue"><span class="bold">Совет</span> &#8212; четко определять проблему. Например, если говорят «потеря времени», то через какое количество времени он будет считать, что это потеря времени.</p>

        <div class="bold">Наводящие вопросы <span class="normal">для формулирования потенциальных проблем потребителя:</span></div>

        <div class="container-text">
            <ul>
                <li class="pl-15">Что такое, с т. зр. потребителя, «слишком затратно»?  То, что отнимает много времени, слишком дорого стоит или требует больших усилий?</li>
                <li class="pl-15">Что заставляет потребителей чувствовать себя плохо? Что их разочаровывает, раздражает или вызывает головную боль?</li>
                <li class="pl-15">Почему существующие ценностные предложения не устраивают потребителей? Чего им не хватает? Какие особенности продукта раздражают и на какие недостатки они указывают?</li>
                <li class="pl-15">С какими основными проблемами сталкиваются потребители? Понимают ли они, как все устроено, есть ли у них трудности с выполнением того или иного действия или они по определенным причинам не хотят выполнять какую-либо задачу?</li>
                <li class="pl-15">С какими негативными социальными последствиями сталкиваются или боятся: потерять лицо, влияние, доверие или статус?</li>
                <li class="pl-15">Какие риски имеют значение для потребителей? Их пугают потенциальные финансовые, социальные или технические риски? Задают ли они себе вопрос, что может пойти не так?</li>
                <li class="pl-15">Что мешает потребителям спокойно спать? Каковы их главные проблемы, источники переживаний, причины для беспокойства?</li>
                <li class="pl-15">Какие ошибки чаще всего допускают потребители? Может быть, они неправильно используют предлагаемые решения?</li>
                <li class="pl-15">Что мешает потребителям принять ценностное предложение – необходимость предоплаты, отсутствие нужных знаний или какие – иные препятствия?</li>
            </ul>
        </div>

        <div class="bold">3.	<span class="pl-15">Выгоды потребителя – это результаты и преимущества, которые хочет потребитель.</span></div>

        <div class="container-text">

            <p>Выгода бывает: необходимой, ожидаемой или желаемой для потребителя, но может оказаться неожиданной. К выгодам относится функциональное удобство, социальные преимущества, положительные эмоции и сокращение затрат.</p>

            <div>
                <span class="bold">Необходимая выгода</span>
                &#8212; свойство, без которого решение не будет работать (смартфон должен дать возможность позвонить, как минимум).
            </div>

            <div>
                <span class="bold">Ожидаемая выгода</span>
                &#8212; существенные преимущества, которые ожидает получить потребитель, но может обойтись без них. Покупая телефон iPhone, потребитель ждет стиль, удобство и т.д.
            </div>

            <div>
                <span class="bold">Желательная выгода</span>
                &#8212; это то, от чего потребитель не отказался бы (совместимость телефона с другими устройствами).
            </div>

            <div>
                <span class="bold">Неожиданная выгода</span>
                &#8212; преимущества не являются ожидаемыми и желаемыми. Пока Apple не предложил тачскрин, никто и не знал об этом ранее.
