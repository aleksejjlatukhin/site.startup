    <h3 class="header-text">Глоссарий</h3>

    <div class="container-list">

        <ul>
            <li>
                <div>
                    <span>Базовая технология (БТ)</span>
                    &#8212; технология, лежащая в основе создания широкого спектра наукоемкой продукции и прямо не связанная с каким-либо видом конкретных техническим систем.
                </div>
            </li>
            <li>
                <div>
                    <span>Глубинное интервью (ГИ)</span>
                    &#8212; неформальная личная беседа, проводимая по заранее намеченному плану, побуждающая респондентов к продолжительным и обстоятельным рассуждениям по ряду вопросов.
                </div>
            </li>
            <li>
                <div>
                    <span>Мотив</span>
                    &#8212; осознанное желание решить эту проблему.
                </div>
            </li>
            <li>
                <div>
                    <span>Потенциальное решение (ПР)</span>
                    &#8212; образ продукта, который является одним из возможных вариантов решения этой проблемы.
                </div>
            </li>
            <li>
                <div>
                    <span>Потребность</span>
                    &#8212; внутреннее ощущение дефицита чего-либо, что необходимо для жизнедеятельности человека и осознается как проблема.
                </div>
            </li>
            <li>
                <div>
                    <span>Проблемное интервью (ПИ)</span>
                    &#8212; это первый и самый главный этап процесса customer development (развитие потребителя). Его задача – выяснить у клиентов, что у них болит.
                    А точнее – выявить сегменты целевой аудитории, у которых есть повторяющиеся паттерны ситуаций, в которых возникает проблема.
                </div>
            </li>
            <li>
                <div>
                    <span>Сегментация</span>
                    &#8212; процесс выявления определенных групп потребителей, которые обладают близкими потребностями, похожим покупательским поведением и отличительными характеристиками.
                </div>
            </li>
            <li>
                <div>
                    <span>Целевой сегмент рынка (ЦСР)</span>
                    &#8212; одна или несколько групп потребителей,  для которых продукт конкретной компании в данном месте, в данное время и в данных условиях подходит наилучшим образом.
                </div>
            </li>
        </ul>

