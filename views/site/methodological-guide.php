        <div class="bold">А участников можно тех же пригласить?</div>

        <p>
            Можно и тех же, если к ним остались вопросы. Или можно найти для каждого метода отдельную группу участников,
            чтобы охватить большую аудиторию. Когда нужно собрать разную информацию и времени много, можно использовать несколько
            методов исследования. А если команда большая, то можно разделиться и работать одновременно: например, пока один
            проводит глубинное интервью, другой модерирует фокус-группы, а кто-то наблюдает за пользователями.
        </p>

        <p>
            Таким образом, нужно последовательно провести интервью с каждым из выбранных респондентов. Если у вас есть команда
            из нескольких человек, то поделив весь список намеченных респондентов, выполнить Шаг 3 можно быстрее. Но предварительно
            все интервьюеры должны быть проинструктированы как проводить интервью: задание, зафиксированное в Шагах 1 и 2.
        </p>

        <p>Запишите, по возможности, то, что отвечали респонденты как можно более дословно и занесите результат в соответствующую форму Spaccel.ru.</p>

        <p>
            После каждого интервью внимательно прочитайте полученную от респондентов информацию и впишите
            в поле «Варианты проблем» возможные проблемы, с которыми, на ваш взгляд, сталкивается респондент в своей жизни.
        </p>

    </div>

    <!--4. Этап 3. Генерация гипотез проблем сегментов-->
    <h3 class="header-text" id="generating_hypotheses_for_segment_problems">4.<span>Этап 3. Генерация гипотез проблем сегментов</span></h3>

    <div class="container-list">

        <div class="simple-block">
            <p>
                <span>Задача:</span>
                Сформировать список гипотез, которые должны быть протестированы на следующей стадии.
            </p>
            <p>
                <span>Результат:</span>
                Одна или несколько сформулированных и проверенных гипотез, внесенные в форму на платформе Spaccel.ru.
            </p>
        </div>

        <br>

        <p>
            Исходной информацией для формулировки гипотез проблем сегментов является информационная таблица, где собраны
            все «Варианты проблем» по материалам всех респондентов. Перед формулировкой гипотезы проблемы проанализируйте
            результаты, собранные в поле «Варианты проблем».
        </p>

        <div class="bold">Общие рекомендации как анализировать результаты ГИ:</div>

        <div class="container-text">
            <ol>
                <li><span class="pl-15">Сводим все в таблицу и смотрим, где есть схожие и противоположные ответы на вопросы.</span></li>
                <li><span class="pl-15">Ищем одинаковые. Тогда мы выясняем, есть ли у пользователей схожая проблема.</span></li>
                <li><span class="pl-15">Если в процессе проведения интервью с одним респондентом выявили проблему, то желательно включить в сценарий проведения с другими респондентами вопрос, есть ли у них выявленная проблема.</span></li>
                <li>
                    <span class="pl-15">Помните, что проблема может лежать в другой плоскости и не в том месте, про которое спрашивали.</span>
                    <p class="mt-10 pl-25">
                        <span class="bold">Пример:</span>
                        Есть стартап, который помогает делать on-line трансляции в интернете. Это делают, потому что есть ожидание,
                        что их никто не умеет делать технически. Поэтому их мало. Но реальность – нет аудитории. Нет интереса.
                        А проблема в том, что просто не умеют привлечь аудиторию.
                    </p>
                </li>
                <li>
                    <span class="pl-15">Может быть выявлена проблема, связанная с тем, что у собеседников разный лексикон. Т.е. иногда будущий пользователь системы не может правильно определить, что ему нужно, потому что он не владеет лексиконом.</span>
                    <p class="mt-10 pl-25">
                        <span class="bold">Пример:</span>
                        Пользователям не нужна CRM система, а нужна система, как они говорят, учет продаж,  что у них с воронкой продаж,
                        контроль менеджеров. Т.е. они называют CRM другими словами.
                    </p>
                </li>
            </ol>
        </div>

        <p class="bold blue">
            Сформулируйте гипотезы проблемы для проверки. Правильная гипотеза (потребности, проблемы) состоит из: предположения,
            действия для проверки, метрики результата и вывода.
        </p>

        <p class="bold">Примеры:</p>

        <p>
            <span class="bold">Сетевой магазин.</span>
            Гипотеза: минимум 20% офисов продаж узнают о новых акциях с задержкой больше трех дней, либо вообще не узнают — из-за
            этого продажи не растут, либо падают. Чтобы найти решение, мы проведем интервью с менеджерами из десяти точек продаж
            в одном регионе. Если семеро и больше подтвердят гипотезу, то сделаем пилотный запуск системы быстрого оповещения точек
            продаж об акциях через мессенджер, после чего проверим, как изменился уровень продаж.
        </p>

        <p>
            <span class="bold">Магазин книг на английском языке.</span>
            Гипотеза: читатели испытывают трудности при заказе книг на Amazon из-за долгой и дорогой доставки. Мы проведем опрос
            среди людей, которые читают на английском. Если больше 70% ответят о долгой и/или дорогой доставке книг с Amazon, гипотезу
            считаем подтвержденной и запускаем тестовую рекламную кампанию на сайт по продаже книг с быстрой и недорогой доставкой книг
            в оригинале.
        </p>

    </div>

    <!--5. Этап 4. Подтверждение гипотез проблем сегментов-->
    <h3 class="header-text" id="confirmation_of_segment_problem_hypotheses">5.<span>Этап 4. Подтверждение гипотез проблем сегментов</span></h3>

    <div class="container-list">

        <div class="simple-block">
            <p>
                <span>Задача:</span>
                Подтвердить или опровергнуть выдвинутые гипотезы проблем сегментов.
            </p>
            <p>
                <span>Результат:</span>
                Получение результата о подтверждении выдвинутых гипотез, внесенного в форму на платформе Spaccel.ru.
            </p>
        </div>

        <br>

        <p class="bold blue">Общий материал перед этапом.</p>

        <div class="bold">Подтверждение гипотез проблем сегментов выполняется с помощью проблемного интервью (ПИ).</div>

        <p>
            <span class="bold">Цель ПИ</span>
            &#8212; подтвердить или опровергнуть гипотезы, которые важны для дальнейших действий по продукту. Ниже для лучшего восприятия приведена таблица с примерами гипотез для проверки:
        </p>

        <div style="width: 100%; overflow: auto;">
            <table>
                <tr>
                    <th>Предположение</th>
                    <th>Действие для проверки</th>
                    <th>Метрика результата</th>
                </tr>
                <tr>
                    <td></td>
                    <td>Что нужно сделать, чтобы проверить гипотезу?</td>
                    <td>Какой результат покажет, что гипотеза верна?</td>
                </tr>
                <tr>
                    <td>Средний класс и люди с высоким достатком хотят питаться свежими овощами, но не доверяют качеству продуктов в магазинах.</td>
                    <td>Задать вопросы: из чего состоит рацион, где они покупают овощи, как они их выбирают, устраивает ли качество</td>
                    <td>Больше 70% опрошенных выразит неудовлетворенность качеством продуктов в магазинах</td>
                </tr>
                <tr>
                    <td>Пользователям сложно определиться с выбором, когда по заданным параметрам они получают слишком много предложений</td>
                    <td>Задать вопросы о последнем опыте подбора жилья, каким образом они это делали, как проходил процесс подбора, из какого количества вариантов выбирали, сколько времени это заняло, что было важно при выборе, и какие трудности возникли в процессе.</td>
