        <div class="bold">Вопросы для продумывания выгод потребителя:</div>

        <div class="container-text">
            <ul>
                <li class="pl-15">Что наиболее ценно для потребителей с точки зрения времени, денег и усилий?</li>
                <li class="pl-15">Какой уровень качества они ожидают получить, и чего они хотят в большей или меньшей степени?</li>
                <li class="pl-15">Чем существующее ЦП привлекает потребителей? Какие именно его аспекты их радуют? Каких эксплуатационных характеристик и уровня качества они ожидают?</li>
                <li class="pl-15">Что облегчает жизнь или работу потребителям? Можно ли упростить процесс освоения продукта, предоставить больше услуг или снизить стоимость владения?</li>
                <li class="pl-15">Каких положительных социальных эффектов ожидают потребители? Что позволяет им хорошо выглядеть? Что укрепляет их положение или повышает статус?</li>
                <li class="pl-15">На что потребители обращают внимание в первую очередь – дизайн, гарантию, конкретные характеристики или количество функций?</li>
                <li class="pl-15">О чем мечтают потребители? Чего им хотелось бы достичь или что оказало бы им большую помощь?</li>
                <li class="pl-15">Как потребители измеряют успех и неудачи? Каковы их критерии оценки достижений или затрат?</li>
                <li class="pl-15">Что может сделать ваше ЦП более привлекательным для потребителей? О чем они мечтают – о снижении стоимости, уменьшении вложений, снижении риска или повышении качества?</li>
            </ul>
        </div>

